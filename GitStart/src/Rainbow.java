public class Rainbow
{
    public static void main(String[] args)
    {
        String[] rainbow = {"Красный", "Оранжевый", "Желтый", "Зеленый", "Голубой", "Синий", "Фиолетовый"};
        for (int i = rainbow.length; i >= 1; i--)
        {
            System.out.println(rainbow[i]);
        }
    }
}
